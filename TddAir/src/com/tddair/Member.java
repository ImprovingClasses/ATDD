package com.tddair;

public class Member {

	private String username;
	private String email;

	public String getUserName() {
		return username;
	}

	public void setUserName(String id) {
		this.username = id;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	
	
	
	
}
